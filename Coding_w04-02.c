#include <stdio.h>      //เรียกใช้ไลบรารีมาตรฐานสำหรับ printf

int main() {          //ฟังก์ชันหลักของโปรแกรม      
    int a = 10;                 //ประกาศตัวแปร a ชนิด int และกำหนดค่าเริ่มต้นเป็น 10        
    printf("a = %d", a);        //แสดงค่า a ออกทางหน้อจอ โดยใช้ %d สำหรับตัวแปรชนิด int        
  
    return 0;      //ส่งค่ากลับเมื่อโปรแกรมจบการทำงานเสร็จสิ้น
}